package Gun16;

public class _03_forLoop {
    public static void main(String[] args) {
// o dan 10 kadar sayilarri ekrana yazdiriniz
        //10 dan 0 a kadar sayilari yazdir
        
        
     
        for (int i=0;i<=10;i++)
        {

                System.out.println("i = " + i);
            }
        
        for (int j=10 ;j>=0;j--)
        {


            System.out.println("j = " + j);
        }
    }
}
